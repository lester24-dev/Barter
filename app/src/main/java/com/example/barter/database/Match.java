package com.example.barter.database;

public class Match {
    String product_category, product_year, product_price1, product_price2, product_address;

    public String getProduct_category() {
        return product_category;
    }

    public void setProduct_category(String product_category) {
        this.product_category = product_category;
    }

    public String getProduct_year() {
        return product_year;
    }

    public void setProduct_year(String product_year) {
        this.product_year = product_year;
    }

    public String getProduct_price1() {
        return product_price1;
    }

    public void setProduct_price1(String product_price1) {
        this.product_price1 = product_price1;
    }

    public String getProduct_price2() {
        return product_price2;
    }

    public void setProduct_price2(String product_price2) {
        this.product_price2 = product_price2;
    }

    public String getProduct_address() {
        return product_address;
    }

    public void setProduct_address(String product_address) {
        this.product_address = product_address;
    }
}
